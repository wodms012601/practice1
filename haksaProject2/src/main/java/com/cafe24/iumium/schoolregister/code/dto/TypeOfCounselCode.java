package com.cafe24.iumium.schoolregister.code.dto;

public class TypeOfCounselCode {
	private String counselTypeCode;
	private String recordId;
	private String counselTypeName;
	private String counselTypeIsUsed;
	private String counselTypeIsChanged;
	private String counselTypeReasonForChange;
	private String counselTypeChangedDate;
	private String counselTypeRegisteredDate;
	
	public String getCounselTypeCode() {
		return counselTypeCode;
	}
	public void setCounselTypeCode(String counselTypeCode) {
		this.counselTypeCode = counselTypeCode;
	}
	public String getRecordId() {
		return recordId;
	}
	public void setRecordId(String recordId) {
		this.recordId = recordId;
	}
	public String getCounselTypeName() {
		return counselTypeName;
	}
	public void setCounselTypeName(String counselTypeName) {
		this.counselTypeName = counselTypeName;
	}
	public String getCounselTypeIsUsed() {
		return counselTypeIsUsed;
	}
	public void setCounselTypeIsUsed(String counselTypeIsUsed) {
		this.counselTypeIsUsed = counselTypeIsUsed;
	}
	public String getCounselTypeIsChanged() {
		return counselTypeIsChanged;
	}
	public void setCounselTypeIsChanged(String counselTypeIsChanged) {
		this.counselTypeIsChanged = counselTypeIsChanged;
	}
	public String getCounselTypeReasonForChange() {
		return counselTypeReasonForChange;
	}
	public void setCounselTypeReasonForChange(String counselTypeReasonForChange) {
		this.counselTypeReasonForChange = counselTypeReasonForChange;
	}
	public String getCounselTypeChangedDate() {
		return counselTypeChangedDate;
	}
	public void setCounselTypeChangedDate(String counselTypeChangedDate) {
		this.counselTypeChangedDate = counselTypeChangedDate;
	}
	public String getCounselTypeRegisteredDate() {
		return counselTypeRegisteredDate;
	}
	public void setCounselTypeRegisteredDate(String counselTypeRegisteredDate) {
		this.counselTypeRegisteredDate = counselTypeRegisteredDate;
	}
}
