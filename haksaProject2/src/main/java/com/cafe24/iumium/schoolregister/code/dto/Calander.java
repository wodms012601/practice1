package com.cafe24.iumium.schoolregister.code.dto;

public class Calander {
	private String calanderNumber;
	private String recordId;
	private String semesterCode;
	private String calanderBeginDate;
	private String calanderEndDate;
	private String calanderTitle;
	private String calanderContent;
	private String calanderRegisteredDate;
	
	public String getCalanderNumber() {
		return calanderNumber;
	}
	public void setCalanderNumber(String calanderNumber) {
		this.calanderNumber = calanderNumber;
	}
	public String getRecordId() {
		return recordId;
	}
	public void setRecordId(String recordId) {
		this.recordId = recordId;
	}
	public String getSemesterCode() {
		return semesterCode;
	}
	public void setSemesterCode(String semesterCode) {
		this.semesterCode = semesterCode;
	}
	public String getCalanderBeginDate() {
		return calanderBeginDate;
	}
	public void setCalanderBeginDate(String calanderBeginDate) {
		this.calanderBeginDate = calanderBeginDate;
	}
	public String getCalanderEndDate() {
		return calanderEndDate;
	}
	public void setCalanderEndDate(String calanderEndDate) {
		this.calanderEndDate = calanderEndDate;
	}
	public String getCalanderTitle() {
		return calanderTitle;
	}
	public void setCalanderTitle(String calanderTitle) {
		this.calanderTitle = calanderTitle;
	}
	public String getCalanderContent() {
		return calanderContent;
	}
	public void setCalanderContent(String calanderContent) {
		this.calanderContent = calanderContent;
	}
	public String getCalanderRegisteredDate() {
		return calanderRegisteredDate;
	}
	public void setCalanderRegisteredDate(String calanderRegisteredDate) {
		this.calanderRegisteredDate = calanderRegisteredDate;
	}
}
